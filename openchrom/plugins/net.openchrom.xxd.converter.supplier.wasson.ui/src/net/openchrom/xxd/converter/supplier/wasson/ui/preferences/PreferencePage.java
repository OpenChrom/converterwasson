package net.openchrom.xxd.converter.supplier.wasson.ui.preferences;

import org.eclipse.jface.preference.FieldEditorPreferencePage;
import org.eclipse.jface.preference.IntegerFieldEditor;
import org.eclipse.ui.IWorkbench;
import org.eclipse.ui.IWorkbenchPreferencePage;

import net.openchrom.xxd.converter.supplier.wasson.preferences.PreferenceSupplier;
import net.openchrom.xxd.converter.supplier.wasson.ui.Activator;

public class PreferencePage extends FieldEditorPreferencePage implements IWorkbenchPreferencePage {

	public PreferencePage() {
		super(GRID);
		setPreferenceStore(Activator.getDefault().getPreferenceStore());
		setDescription("Wasson-ECE Chromatogram Converter");
	}

	/**
	 * Creates the field editors. Field editors are abstractions of the common
	 * GUI blocks needed to manipulate various types of preferences. Each field
	 * editor knows how to save and restore itself.
	 */
	public void createFieldEditors() {

		addField(getIntegerFieldEditor(PreferenceSupplier.P_VALUE, "Value", PreferenceSupplier.MIN_VALUE, PreferenceSupplier.MAX_VALUE));
	}

	private IntegerFieldEditor getIntegerFieldEditor(String name, String label, int min, int max) {

		IntegerFieldEditor integerFieldEditor = new IntegerFieldEditor(name, label, getFieldEditorParent());
		integerFieldEditor.setValidRange(min, max);
		return integerFieldEditor;
	}

	/*
	 * (non-Javadoc)
	 * @see
	 * org.eclipse.ui.IWorkbenchPreferencePage#init(org.eclipse.ui.IWorkbench)
	 */
	public void init(IWorkbench workbench) {

	}
}
